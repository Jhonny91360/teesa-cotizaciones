    //Para mostrar todos los datos de la tabla , aqui se pasaria la info a un pdf
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
             DefaultTableModel modelo = (DefaultTableModel) jTableProductos.getModel();

        for (int i = 0; i < modelo.getRowCount(); i++) {
            String producto = (String) modelo.getValueAt(i, 0);
            double precio = (double) modelo.getValueAt(i, 1);
            int cantidad = Integer.parseInt (modelo.getValueAt(i, 2).toString());

            System.out.println("Producto: " + producto + ", Precio: " + precio + ", Cantidad: " + cantidad);
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Error: "+e.toString());
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed
    //Eliminar producto , fila seleccionada
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         int filaSeleccionada = jTableProductos.getSelectedRow();

        if (filaSeleccionada >= 0) {
            // Eliminar la fila seleccionada del modelo
            modeloTabla.removeRow(filaSeleccionada);
            JOptionPane.showMessageDialog(this, "Fila eliminada con Ã©xito.");
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila para eliminar.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

