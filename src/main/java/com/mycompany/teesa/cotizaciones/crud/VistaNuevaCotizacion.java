    //Configuro la tabla directamente con los campos que necesite
     private void configurarTabla() {
        modeloTabla = new DefaultTableModel(
                new Object[][]{}, 
                new String[]{"Producto", "Precio", "Cantidad"}
        ) {
            //Defino la celda que sera editable, en este caso la catidad
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2; // Solo la columna "Cantidad" es editable
            }
        };

        jTableProductos.setModel(modeloTabla); // Asignar modelo a la tabla
    }
     
     public void agregarProducto(String producto, double precio, int cantidad) {
        modeloTabla.addRow(new Object[]{producto, precio, cantidad});
    }

